export const getBrokerById = (id: string): BrokerApplication | undefined => {
  return mockBrokers.find(broker => broker.id === id);
};

export const getTrucksByBrokerId = (brokerId: string): Truck[] => {
  return mockBrokerTrucks[brokerId] || [];
};

export const getLoadsByBrokerId = (brokerId: string): Load[] => {
  return mockBrokerLoads[brokerId] || [];
};

export const getPaymentsByBrokerId = (brokerId: string): Payment[] => {
  return mockBrokerPayments[brokerId] || [];
};

export const getStatsByBrokerId = (brokerId: string): DashboardStats | undefined => {
  return mockBrokerDashboardStats[brokerId];
};

export const getApprovedBrokers = (): BrokerApplication[] => {
  return mockBrokers.filter(broker => broker.status === 'approved');
};

export const getPendingBrokers = (): BrokerApplication[] => {
  return mockBrokers.filter(broker => ['submitted', 'under_review'].includes(broker.status));
};

export const getRejectedBrokers = (): BrokerApplication[] => {
  return mockBrokers.filter(broker => broker.status === 'rejected');
};
